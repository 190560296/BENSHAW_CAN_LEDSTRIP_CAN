/**
  * @brief  The application entry point.
  *
  * @retval None
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_TIM3_Init();
  MX_TIM4_Init();
  MX_TIM6_Init();
  MX_USART1_UART_Init();
  MX_CAN_Init();
  /* USER CODE BEGIN 2 */
  Config_CanFilter();
  
	ledstrip1_rgbw_alloff();
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {

  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */
	  if(flag_can)
	  {
		  flag_can = 0;
		  
		  printf("can recieved !\n");
		  can_rxbuf = hcan.pRxMsg->Data[1];
		  
		  res_tag = (can_rxbuf&0x7);
		  res_mov = (can_rxbuf&0x38)>>3;
		  res_color = (can_rxbuf>>6);
		  
		  //printf("tag = %d , mov = %d , color = %d .\n",res_tag,res_mov,res_color);
		  
		  if((res_tag == 0)||(res_tag == positionID))
		  {
			  switch((int)res_color)
			  {
				  case 0:
					  color = &color_white;
					  break;
				  case 1:
					  color = &color_blue;
					  break;
				  case 2:
					  color = &color_red;
					  break;
				  default:
					  color=&color_null;
					  break;
			  }
				  
			  switch(res_mov)
			  {
				  case 0:
						break;
				  case 1:
						ledstrip1_rgbw_alloff();
						ledstrip2_rgbw_alloff();
						break;
				  case 2:
						ledstrip1_rgbw_perframe(lednumber1,lednumber1,color,color);
						ledstrip2_rgbw_perframe(lednumber2,lednumber2,color,color);
						break;
				  case 3:
						ledgroup_rgbw_flow(lednumber1,DELAY_MS1,color,&color_null);
						break;
				  case 4:
						ledgroup_rgbw_flow(lednumber1,DELAY_MS2,color,&color_null);
						break;
				  default:
						break;
			  }
			  
			  sendCanData(canTxID,feedback_CAN_Buffer,8);
				 
			  res_tag = 0xff;              
			  res_mov = 0xff;
			  res_color = 0xff;
		  }
	}

  }
  /* USER CODE END 3 */

}

